package sept.ex_09092024;

public class FirstProgram {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println(2*2);
        System.out.println(2/2);

        /*
         * To execute using the notepad
         * create a notepad and write the program and save the file with .java extension
         * then open command prompt and change the directory to the .java file written
         * then type javac ClassName.java and press enter
         * then type java ClassName and press enter the code will execute and output is displayed in console
         */
    }
}