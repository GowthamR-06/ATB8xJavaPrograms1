package sept.ex_16092024;

public class Lab037_Compound_Assignment_Operator {
    public static void main(String[] args) {

        //compound operator
        // += , -=, *- , /=

        int a =10;
        int b =11;
        int c =12;
        int d = 13;

        a += 20;// a = a +20;
        b -=  12 ;// b= b-12;
        c *= 10; // c = c * 10;
        d /= 2; //d = d /2;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
