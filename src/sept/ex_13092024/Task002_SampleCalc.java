package sept.ex_13092024;

public class Task002_SampleCalc {
    public static void main(String[] args) {

        double a =34;
        double b=10;

        System.out.printf("%f + %f = %f",a,b,a+b);
        System.out.println();
        System.out.printf("%f - %f = %f",a,b,a-b);
        System.out.println();
        System.out.printf("%f * %f = %f",a,b,a*b);
        System.out.println();
        System.out.printf("%f / %f = %f",a,b,a/b);
        System.out.println();
        System.out.printf("%f modulus %f = %f%n",a,b,a%b);
        System.out.println();
    }
}
